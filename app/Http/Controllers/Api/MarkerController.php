     */
    public function index(Request $request)
    {

        $bbox = $request->get('bbox');

        if ($bbox) {
            $markers = new MarkerResourceCollection(Marker::inBoundedBox($bbox)->get());
            return response()->json($markers);
        } else {
            abort(400, 'No bound box coordinates');
        }
    }

